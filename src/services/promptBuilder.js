// خصم الزبون (VIP من الكاشير برقم هاتفه، أو كود خصم كتبه بنفس الرسالة) — منفصل عن البرومبت الثابت لنفس سبب
// بلوك بيانات الزبون فوق (ما نكسر الـcache)، وكمان عشان قائمة الأكواد نفسها ما تنكشف أبداً لموديل الذكاء
// الاصطناعي — هو بس بيشوف نتيجة الكود يلي الزبون كتبه فعلاً هالرسالة، مش كل الأكواد الموجودة بالنظام.
function buildDiscountSection(discountContext) {
  if (!discountContext) return "";
  const lines = ["خصم الزبون (طبّقه فوراً بدون ما تحتاج تسأل، واذكره للزبون بوضوح بردك، واطرحه من المجموع بأداة record_order):"];

  if (discountContext.vip) {
    lines.push(`- هاد الزبون عميل مميز (VIP) بالنظام: خصم ${discountContext.vip.percent}% على كل طلبه.`);
  }
  if (discountContext.code) {
    const desc = discountContext.code.kind === "percent" ? `${discountContext.code.value}%` : `${discountContext.code.value} د.أ`;
    lines.push(`- الزبون كتب كود خصم "${discountContext.code.code}" وهو صحيح وفعّال: خصم ${desc}.`);
  }

  return lines.join("\n");
}

